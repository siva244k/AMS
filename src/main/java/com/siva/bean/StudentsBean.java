package com.siva.bean;

public class StudentsBean {

	int id;
	String name;
	String roll;
	int st_cls_id;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getRoll() {
		return roll;
	}

	public void setRoll(String roll) {
		this.roll = roll;
	}

	public int getSt_cls_id() {
		return st_cls_id;
	}

	public void setSt_cls_id(int st_cls_id) {
		this.st_cls_id = st_cls_id;
	}

}
